// EQUAL
console.log('3' == 3);

/*
    - coercion is the process of converting a value from one type to another
*/

// STRICT EQUAL
console.log(3 === 3);
console.log('3' === 3);

// NOT EQUAL
console.log('3' != 3);

// STRICT NOT EQUAL
console.log('3' !== 4);

// BELOW MUST BE NUMBERS STRINGS WILL THROW AN ERROR
// GREATER THAN
console.log(3 > 2);

// LESS THAN
console.log(2 < 3);

// GREATER THAN OR EQUAL TO
console.log(3 >= 2);
console.log(3 >= 3);

// LESS THAN OR EQUAL TO
console.log(2 <= 3);
console.log(2 <= 2);


